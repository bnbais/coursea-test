# Comparison operator are used to compare two statements
# <,>,==,<=,>=,!=,

print(2>3) #It will return 'False'
print(2<3) # It will return 'True'
print(2==2) # It will return 'True'
print(2!=2) # It will return 'False'
print(2>=3) # It will return 'False'
print(2<=3) # It will return 'True'
print(2>=1) # It will return 'True'